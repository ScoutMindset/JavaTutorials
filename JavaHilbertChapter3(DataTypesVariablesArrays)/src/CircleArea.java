public class CircleArea
{
    public static void main(String args[])
    {
        double pi;
        double r;
        double circleArea;

        r = 10.8; // Radius of the circle
        pi = 3.1416; // This is the value of the Pi constant
        circleArea = pi * r * r;
        System.out.println("The area of the circle is " + circleArea);
    }
}
